def dump_scalar(level, v, name):
   global dump_level
   if level <= dump_level:
      buf = bytearray()
      buf += struct.pack("i", len(name))
      buf += name.encode('ascii')
      buf += struct.pack("i", 1)
      buf += struct.pack("i", 1)
      buf += struct.pack("d", v)
      dump_file.write(buf)
   

def dump_array(m, name):
   buf = bytearray()
   rows = m.shape[0]
   buf += struct.pack("i", len(name))
   buf += name.encode('ascii')
   buf += struct.pack("i", rows)
   buf += struct.pack("i", 1)
   for i in range(0, rows):
      buf += struct.pack("d", m[i])
   dump_file.write(buf)
   
def dump_matrix(m, name):
   buf = bytearray()
   rows = m.shape[0]
   cols = m.shape[1]
   buf += struct.pack("i", len(name))
   buf += name.encode('ascii')
   buf += struct.pack("i", rows)
   buf += struct.pack("i", cols)
   for i in range(0, rows):
      for j in range(0, cols):
         buf += struct.pack("d", m[i][j])
   dump_file.write(buf)

def dump(level, m, name):
   global dump_level
   if level <= dump_level:
      axes = len(m.shape)
      if (axes == 1):
         dump_array(m, name)
      elif (axes == 2):
         dump_matrix(m, name)

